    private boolean caixaConfirmacao(String acaoConfirmar) {

        int resposta = JOptionPane.showConfirmDialog(this, "Deseja confirmar essa ação? ", acaoConfirmar.toUpperCase(), JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (resposta == JOptionPane.YES_OPTION) {
            return true;
        } else {
            return false;
        }
    }


