    private void atualizaTabela() throws SQLException {
        DoramaDAO doramaDAO = new DoramaDAO();

        listaDoramas = doramaDAO.localizarDorama();

        //PARA LISTA
        DefaultListModel<String> modelo = new DefaultListModel<>();
        listDoramas.setModel(modelo);
        //
        listDoramas.removeAll();

        if (listaDoramas.isEmpty()) {
            return;
        }

        for (int i = 0; i < listaDoramas.size(); i++) {
            modelo.addElement(listaDoramas.get(i).getTitulo());
        }

    }

    private void atualizaInfos() throws SQLException {
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yy");

        Integer selecionado = listDoramas.getSelectedIndex(); //retorna int
        doramaSelecionado = listaDoramas.get(selecionado);

        String stringIcon;

        if (doramaSelecionado.getImagem() == null) {
            stringIcon = "C:\\Users\\Aluno\\OneDrive - EDU - Organização Educacional Barão de Mauá\\Facul\\6º Sem\\Padrões e Paradigmas de Programação\\Kleob\\Kleob\src\\capaDorama\\default.jpg";
        } else {
            stringIcon = "C:\\Users\\Aluno\\OneDrive - EDU - Organização Educacional Barão de Mauá\\Facul\\6º Sem\\Padrões e Paradigmas de Programação\\Kleob\\Kleob\src\\capaDorama\\" + doramaSelecionado.getImagem();
        }

        ImageIcon icon = new ImageIcon(stringIcon);//scaling image to fit in jlable

        Image img = icon.getImage();
        Image imgScale = img.getScaledInstance(capa.getWidth(), capa.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(imgScale);
        capa.setIcon(scaledIcon);

        labelTitulo.setText("<html>" +doramaSelecionado.getTitulo() + " | Eps: " + doramaSelecionado.getEpisodio() + " | " + doramaSelecionado.getGenero()+ "</html>");
        labelDescricao.setText("<html>" + doramaSelecionado.getDescricao() + "</html>");

        ForumDAO forumDAO = new ForumDAO();
        listaComentarios = forumDAO.localizarForum(doramaSelecionado.getIdDorama());

        DefaultListModel<String> modeloComent = new DefaultListModel<>();
        listComentarios.setModel(modeloComent);

        listComentarios.removeAll();
        
        alteraEstrela(doramaSelecionado.getNota());

        for (int i = 0; i < listaComentarios.size(); i++) {
            modeloComent.addElement("<html>" + listaComentarios.get(i).getNomeUsuario()
                    + "-" + format.format(listaComentarios.get(i).getDataComent())
                    + "-" + listaComentarios.get(i).getComentario() + "</html>");
        }

    }

