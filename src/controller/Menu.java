    private void botaoEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEnviarActionPerformed
        boolean enviar = caixaConfirmacao("Enviar Comentário");
        if (enviar) {

            if (novoComentario.getText().isBlank() || novoComentario.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por gentileza, insira um comentário válido.", "Comentário", JOptionPane.INFORMATION_MESSAGE);
            } else {
                ForumDAO forumDAO = new ForumDAO();
                Forum novoForum = new Forum(doramaSelecionado.getIdDorama(), Sessao.getUsuario().getIdUsuario(), novoComentario.getText());

                forumDAO.adicionaComentario(novoForum);

            }
            novoComentario.setText("");
            try {
                atualizaInfos();
            } catch (SQLException ex) {
                Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_botaoEnviarActionPerformed

