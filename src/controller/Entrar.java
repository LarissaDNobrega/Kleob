    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
        if (!validaTela()) {
            return;
        }

        UsuarioDAO usuarioDAO = new UsuarioDAO();
        String senhaBanco = usuarioDAO.validarAcesso(TFemail.getText());

        char[] senha = password.getPassword();
        Boolean acesso = Arrays.equals(senha, senhaBanco.toCharArray());

        if (!acesso) {
            JOptionPane.showMessageDialog(this, "Email e/ou Senha inválidos.", "Validação", JOptionPane.INFORMATION_MESSAGE);
            reiniciaTela();
        } else {
            Menu next = null;
            try {
                next = new Menu();
            } catch (SQLException ex) {
                Logger.getLogger(Entrar.class.getName()).log(Level.SEVERE, null, ex);
            }
            Sessao.setEmail(TFemail.getText());
            try {
                Sessao.setUsuario(usuarioDAO.selecionaUsuario(Sessao.getEmail()));
            } catch (SQLException ex) {
                Logger.getLogger(Entrar.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.setVisible(false);
            next.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_botaoEntrarActionPerformed

