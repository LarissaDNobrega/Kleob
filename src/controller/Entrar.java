    private boolean validaTela() {

        if (!Validadores.validaEmail(TFemail.getText())) {
            Console.escreve(TFemail.getText());
            JOptionPane.showMessageDialog(this, "Informe um email válido.", "Validação", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }

        return true;
    }

    private void reiniciaTela() {
        TFemail.setText("");
        password.setText("");
        TFemail.requestFocus();
    }
