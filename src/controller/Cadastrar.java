    private boolean validaTela(){
        
        if(!Validadores.validaEmail(TFnomeUsuario.getText())){
            JOptionPane.showMessageDialog(this, "Informe um nome de usu√°rio v√°lido.", "Valida√ß√£o", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if(!Validadores.validaNome(TFnomeCompleto.getText())){
            JOptionPane.showMessageDialog(this, "Informe um nome v√°lido.", "Valida√ß√£o", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if (!Validadores.validaEmail(TFEmail.getText())) {
            JOptionPane.showMessageDialog(this, "Informe um email v√°lido.", "Valida√ß√£o", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if(Arrays.equals(password.getPassword(), password2.getPassword()) == false || 
                password.getPassword().length == 0){
            JOptionPane.showMessageDialog(this, "Verifique se as senhas s√£o correspondentes e preenchidas.", "Valida√ß√£o", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        
        if(!Validadores.validaData(JFTdataNascimento.getText())){
            JOptionPane.showMessageDialog(this, "Informe a data no formato DD/MM/YYYY.", "Valida√ß√£o", JOptionPane.INFORMATION_MESSAGE);
            return false;
            
        }

        return true;
    }

